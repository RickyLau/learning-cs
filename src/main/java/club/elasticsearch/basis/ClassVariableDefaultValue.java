package club.elasticsearch.basis;

/**
 * Create java object with 8 primitive types member variables.
 *
 * @author <a href="mailto:RickyLau5016@gmail.com">Ricky Lau</a>
 * @version 0.0.1 2020-03-19 00:40:57
 */
public class ClassVariableDefaultValue {
    /**
     * without initial primitive types.
     */
    private boolean booleanDefaultValue;
    private byte byteDefaultValue;
    private char charDefaultValue;
    private short shortDefaultValue;
    private int intDefaultValue;
    private long longDefaultValue;
    private float floatDefaultValue;
    private double doubleDefaultValue;

    public boolean isBooleanDefaultValue() {
        return booleanDefaultValue;
    }

    public byte getByteDefaultValue() {
        return byteDefaultValue;
    }

    public char getCharDefaultValue() {
        return charDefaultValue;
    }

    public short getShortDefaultValue() {
        return shortDefaultValue;
    }

    public int getIntDefaultValue() {
        return intDefaultValue;
    }

    public long getLongDefaultValue() {
        return longDefaultValue;
    }

    public float getFloatDefaultValue() {
        return floatDefaultValue;
    }

    public double getDoubleDefaultValue() {
        return doubleDefaultValue;
    }
}
